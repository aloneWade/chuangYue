    /**
     * 通过构造方法初始化各个成员属性
     */
    public IdCardInfoExtractor(String idCard) {
        if (isValidatedAllIdcard(idCard)) {
            if (idCard.length() == 15)
                idCard = convertIdcarBy15bit(idCard);

            // 获取省份
            String provinceId = idCard.substring(0, 2);
            Set<String> provinceKey = cityCodeMap.keySet();

            for (String id : provinceKey) {
                if (id.equals(provinceId)) {
                    province = cityCodeMap.get(id);
                    break;
                }
            }

            // 获取省份
            String cityId = idCard.substring(0, 4);
            Set<String> cityKey = cityCodeMap.keySet();

            for (String id : cityKey) {
                if (id.equals(cityId)) {
                    city = cityCodeMap.get(id);
                    break;
                }
            }

            // 获取省份
            String regionId = idCard.substring(0, 6);
            Set<String> regionKey = cityCodeMap.keySet();

            for (String id : regionKey) {
                if (id.equals(regionId)) {
                    region = cityCodeMap.get(id);
                    break;
                }
            }

            // 获取性别
            String id17 = idCard.substring(16, 17);
            gender = Integer.parseInt(id17) % 2 != 0 ? "男" : "女";

            // 获取出生日期
            String birthday = idCard.substring(6, 14);

            try {
                this.birthday = new SimpleDateFormat("yyyyMMdd").parse(birthday);
            } catch (ParseException e) {
                e.printStackTrace();
            }

            GregorianCalendar currentDay = new GregorianCalendar();
            currentDay.setTime(this.birthday);

            year = currentDay.get(Calendar.YEAR);
            month = currentDay.get(Calendar.MONTH) + 1;
            day = currentDay.get(Calendar.DAY_OF_MONTH);

            // 获取年龄
            currentDay.setTime(new Date());
            this.age = currentDay.get(Calendar.YEAR) - year;
        }
    }

